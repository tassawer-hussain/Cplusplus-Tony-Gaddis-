//  Problem 016.
//  Diamond Pattern
//  Write a program that displays the following pattern:
//      *
//     ***
//    *****
//   *******
//    *****
//     ***
//      *

#include <iostream>

using namespace std;

int main() {
    cout << "     *\n" <<
        "    ***\n" <<
        "   *****\n" <<
        "  *******\n" <<
        " *********\n" <<
        "  *******\n" <<
        "   *****\n" <<
        "    ***\n" <<
        "     *\n\n";

    return 0;
}
